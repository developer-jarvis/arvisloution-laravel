<script>
    function toggleAccordions() {
        const hiddenAccordions = document.getElementById('hiddenAccordions');
        const seeMoreBtn = document.getElementById('seeMoreBtn');
        const seeLessBtn = document.getElementById('seeLessBtn');

        if (hiddenAccordions.classList.contains('d-none')) {
            hiddenAccordions.classList.remove('d-none');
            seeMoreBtn.classList.add('d-none');
            seeLessBtn.classList.remove('d-none');
        } else {
            hiddenAccordions.classList.add('d-none');
            seeMoreBtn.classList.remove('d-none');
            seeLessBtn.classList.add('d-none');
        }
    }
</script>
